package retail.admin.service;

public class AdminVO {
	
	private String ADMIN_ID;			// 관리자 ID 
	private String ADMIN_PASS;		// 비밀번호 
	private String ADMIN_GRADE;	// 관리등급	
	private String ADMIN_NAME;		// 관리자 이름	
	
	public String getADMIN_ID() {
		return ADMIN_ID;
	}
	public void setADMIN_ID(String aDMIN_ID) {
		ADMIN_ID = aDMIN_ID;
	}
	public String getADMIN_PASS() {
		return ADMIN_PASS;
	}
	public void setADMIN_PASS(String aDMIN_PASS) {
		ADMIN_PASS = aDMIN_PASS;
	}
	public String getADMIN_GRADE() {
		return ADMIN_GRADE;
	}
	public void setADMIN_GRADE(String aDMIN_GRADE) {
		ADMIN_GRADE = aDMIN_GRADE;
	}
	public String getADMIN_NAME() {
		return ADMIN_NAME;
	}
	public void setADMIN_NAME(String aDMIN_NAME) {
		ADMIN_NAME = aDMIN_NAME;
	}	
}
